function dXdt=multiple_element_sys (t,X,M1p,M2p,B1p,B2p,B3p,K1p,K2p,Fap)
Fa=300*Fap/100;     		%(N)
M1=750*M1p/100;     		%(Kg)
M2=750*M2p/100; 		%(Kg)
B1=20*B1p/100;     		%(Nsec/m)
B2=20*B2p/100;		%(Nsec/m)
B3=30*B3p/100; 		%(Nsec/m)
K1=15*K1p/100;      		%(N/m)
K2=15*K2p/100;      		%(N/m)		
dXdt(1,1)=X(2);
dXdt(2,1)=-K2/M2*X(1)-((B1+B2)/M2)*X(2)+B3*X(4)/M2;
dXdt(3,1)=X(4);
dXdt(4,1)=B3/M1*X(2)-K1/M1*X(3)-((B1+B3)/M1)*X(4)+Fa/M1;
